<script>
document.addEventListener('DOMContentLoaded', function() {
    const calendarData = @json($calendarData);
    const selectedDate = @json($selectedDate);
    let currentDate = new Date('{{ $currentMonth->format('Y-m-01') }}');

    function renderCalendar() {
        const year = currentDate.getFullYear();
        const month = currentDate.getMonth();

        // Update header
        const monthYear = new Date(year, month);
        const monthName = monthYear.toLocaleDateString('id-ID', { month: 'long', year: 'numeric' });
        document.getElementById('monthYear').textContent = monthName.charAt(0).toUpperCase() + monthName.slice(1);

        // Generate calendar
        const firstDay = new Date(year, month, 1).getDay();
        const lastDate = new Date(year, month + 1, 0).getDate();
        const prevLastDate = new Date(year, month, 0).getDate();

        const grid = document.getElementById('calendarGrid');
        grid.innerHTML = '';

        // Previous month days
        for (let i = firstDay - 1; i >= 0; i--) {
            const date = prevLastDate - i;
            const day = document.createElement('div');
            day.className = 'col text-center p-2';
            day.innerHTML = `<small class="text-muted">${date}</small>`;
            grid.appendChild(day);
        }

        // Current month days
        for (let date = 1; date <= lastDate; date++) {
            const dateStr = `${year}-${String(month + 1).padStart(2, '0')}-${String(date).padStart(2, '0')}`;
            const isToday = dateStr === new Date().toISOString().split('T')[0];
            const isSelected = dateStr === selectedDate;
            const hasActivities = calendarData[dateStr];

            const day = document.createElement('div');
            day.className = 'col p-1';

            const btn = document.createElement('button');
            btn.type = 'button';
            btn.className = 'btn btn-sm w-100 position-relative';

            if (isToday) {
                btn.classList.add('btn-primary');
            } else if (isSelected) {
                btn.classList.add('btn-success');
            } else if (hasActivities) {
                btn.classList.add('btn-outline-success');
            } else {
                btn.classList.add('btn-outline-secondary');
            }

            btn.innerHTML = date;
            if (hasActivities) {
                btn.innerHTML += `<span class="position-absolute top-0 end-0 translate-middle badge rounded-pill bg-danger" style="font-size: 0.65rem;">${hasActivities.length}</span>`;
            }

            btn.addEventListener('click', (e) => {
                e.preventDefault();
                window.location.href = `?tanggal=${dateStr}`;
            });

            day.appendChild(btn);
            grid.appendChild(day);
        }

        // Next month days
        const totalCells = grid.children.length;
        const remainingCells = 42 - totalCells;
        for (let date = 1; date <= remainingCells; date++) {
            const day = document.createElement('div');
            day.className = 'col text-center p-2';
            day.innerHTML = `<small class="text-muted">${date}</small>`;
            grid.appendChild(day);
        }
    }

    document.getElementById('prevMonth').addEventListener('click', function() {
        currentDate.setMonth(currentDate.getMonth() - 1);
        renderCalendar();
    });

    document.getElementById('nextMonth').addEventListener('click', function() {
        currentDate.setMonth(currentDate.getMonth() + 1);
        renderCalendar();
    });

    renderCalendar();
});
</script>
